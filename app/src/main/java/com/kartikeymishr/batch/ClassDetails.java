public class ClassDetails {

    String classNumber;
    String className;
    String classroom;
    String faculty;

    public ClassDetails(String classNumber, String className, String classroom, String faculty) {
        this.classNumber = classNumber;
        this.className = className;
        this.classroom = classroom;
        this.faculty = faculty;
    }

}
